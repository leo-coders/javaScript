// '=' -> assignment operator
let shirtPrice = 300;
let pantPrice = 800;

// '+' -> addition operator
// let sum = 5 + 10
let totalPrice = shirtPrice + pantPrice;

// '-' -> subtraction operator
let difference = shirtPrice - pantPrice;

// '*' -> multiplication operator
let multiplication = shirtPrice * pantPrice;

// '/' -> division operator
let division = shirtPrice / pantPrice;

// '%' -> modulo operator
let remainder = shirtPrice % pantPrice;

/**
 * SHORTCUT
 */
// INCREMENT
pantPrice = pantPrice + 200;
// or
pantPrice += 200;

// DECREMENT
shirtPrice = shirtPrice - 100;
// or
shirtPrice -= 100;

// PRODUCT
pantPrice = pantPrice * 2;
// or
pantPrice *= 2;

// DIVIDE
shirtPrice = shirtPrice / 2;
// or
shirtPrice /= 2;

// MODULO
pantPrice = pantPrice % 3;
// or
pantPrice %= 3;

// ADVANCE
// ** -> exponentiation/power operator
console.log(2 ** 4);

/*
 * CONCATENATION or adding multiple string
 */
console.log("Sadman " + "Sakib");
// or
console.log("Sadman" + " Sakib");
// or
console.log("Sadman" + " " + "Sakib");

/**
 * CONDITIONAL OPERATOR
 */
// <, >, <=, >=, ==, ===
console.log(5 > 2); // true
console.log(6 > 10); // false
console.log(6 >= 6); // true
console.log(5 <= 8); // true
console.log("3" == 3); // false
console.log(3 == 3); // true
